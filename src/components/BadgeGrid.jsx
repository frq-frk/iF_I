function BadgeIcon({ iconName, className }) {
  return (
    <svg className={className} fill="none" stroke="currentColor" viewBox="0 0 24 24">
      {icons[iconName] || icons.star}
    </svg>
  );
}

const BadgeGrid = ({ earned = [], loading = false }) => {
  const [tooltip, setTooltip] = useState(null);
  const earnedIds = new Set(earned.map(b => b.id));

  if (loading) {
    return (
      <div className="grid grid-cols-4 gap-2 sm:grid-cols-5 md:grid-cols-7">
        {[...Array(7)].map((_, i) => (
          <div key={i} className="flex flex-col items-center gap-1.5 rounded-xl border border-white/[0.06] bg-white/[0.02] p-3">
            <div className="h-8 w-8 rounded-full animate-pulse-soft bg-white/[0.06]" />
            <div className="h-2.5 w-12 rounded animate-pulse-soft bg-white/[0.04]" />
          </div>
        ))}
      </div>
    );
  }

  return (
    <div className="grid grid-cols-4 gap-2 sm:grid-cols-5 md:grid-cols-7">
      {BADGES.map(badge => {
        const isEarned = earnedIds.has(badge.id);
        const colors = BADGE_COLORS[badge.color];

        return (
          <div
            key={badge.id}
            className="relative"
            onMouseEnter={() => setTooltip(badge.id)}
            onMouseLeave={() => setTooltip(null)}
          >
            <div className={`flex flex-col items-center gap-1.5 rounded-xl border p-3 transition-all ${
              isEarned
                ? `${colors.bg} ${colors.border} shadow-lg ${colors.glow}`
                : 'border-white/[0.04] bg-white/[0.01] opacity-30'
            }`}>
              <div className={`relative flex h-8 w-8 items-center justify-center rounded-full ${
                isEarned ? `${colors.bg}` : 'bg-white/[0.04]'
              }`}>
                <BadgeIcon
                  iconName={badge.icon}
                  className={`h-4 w-4 ${isEarned ? colors.text : 'text-slate-600'}`}
                />
                {!isEarned && (
                  <svg className="absolute -bottom-0.5 -right-0.5 h-3 w-3 text-slate-600" fill="currentColor" viewBox="0 0 20 20">
                    <path fillRule="evenodd" d="M10 1a4.5 4.5 0 00-4.5 4.5V9H5a2 2 0 00-2 2v6a2 2 0 002 2h10a2 2 0 002-2v-6a2 2 0 00-2-2h-.5V5.5A4.5 4.5 0 0010 1zm3 8V5.5a3 3 0 10-6 0V9h6z" clipRule="evenodd" />
                  </svg>
                )}
              </div>
              <span className={`text-[0.5625rem] font-medium text-center leading-tight ${
                isEarned ? colors.text : 'text-slate-600'
              }`}>
                {badge.name}
              </span>
            </div>

            {/* Tooltip */}
            {tooltip === badge.id && (
              <div className="absolute bottom-full left-1/2 z-50 mb-2 -translate-x-1/2 whitespace-nowrap rounded-lg border border-white/[0.08] bg-[#0a0a0f]/95 px-3 py-2 text-center shadow-xl backdrop-blur-md">
                <p className={`text-xs font-semibold ${isEarned ? colors.text : 'text-slate-400'}`}>
                  {badge.name}
                </p>
                <p className="mt-0.5 text-[0.625rem] text-slate-500">{badge.description}</p>
                {!isEarned && (
                  <p className="mt-1 text-[0.5625rem] text-slate-600">Not yet earned</p>
                )}
                <div className="absolute left-1/2 top-full -translate-x-1/2 border-4 border-transparent border-t-white/[0.08]" />
              </div>
            )}
          </div>
        );
      })}
    </div>
  );
};

export default BadgeGrid;
